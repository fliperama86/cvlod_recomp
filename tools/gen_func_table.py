#!/usr/bin/env python3
"""
Generate function lookup table (funcs.cpp) from funcs.h declarations.

This creates a hash map of function addresses to function pointers,
enabling the LOOKUP_FUNC macro to resolve indirect calls at runtime.
"""

import re
import sys
from pathlib import Path


# Runtime thread entry addresses observed through osCreateThread do not always
# match the ELF symbol addresses used by N64Recomp. Keep those fixups here so
# regenerating src/funcs.cpp preserves the lookup behavior.
ADDRESS_ALIASES = [
    (0x800186DC, 0x8001783C, "func_8001783C", "idle thread entry"),
    (0x80018770, 0x800178D0, "func_800178D0", "main thread entry"),
    (0x80019B14, 0x80018774, "func_80018774", "thread 19 entry"),
    (0x80019D58, 0x800189B8, "func_800189B8", "thread 18 entry"),
    (0x80018D28, 0x80017DB8, "func_80017DB8", "thread 16 entry"),
]


def parse_funcs_header(header_path: Path) -> list[tuple[str, int]]:
    """Parse funcs.h and extract function names with their addresses."""
    functions = []

    with open(header_path, 'r') as f:
        content = f.read()

    # Match function declarations: void func_XXXXXXXX(...)
    # Also match D_XXXXXXXX, static_N_XXXXXXXX, and L_XXXXXXXX patterns
    pattern = r'^void\s+((?:func_|D_|static_\d+_|L_)([0-9A-Fa-f]{8}))\s*\('

    for line in content.split('\n'):
        match = re.match(pattern, line.strip())
        if match:
            func_name = match.group(1)
            addr_str = match.group(2)
            try:
                addr = int(addr_str, 16)
                functions.append((func_name, addr))
            except ValueError:
                pass

    return functions

def generate_cpp(functions: list[tuple[str, int]], output_path: Path):
    """Generate the C++ source file with the function lookup table."""

    # Sort by address for potential binary search optimization
    functions.sort(key=lambda x: x[1])

    with open(output_path, 'w') as f:
        f.write('''/**
 * CVLoD Recomp - Function Lookup Table
 *
 * Auto-generated by tools/gen_func_table.py
 * Contains mapping from VRAM addresses to function pointers for indirect calls.
 */

#include <cstdint>
#include <unordered_map>
#include "recomp.h"
#include "funcs.h"

// Function lookup table
static std::unordered_map<int32_t, recomp_func_t*> g_function_table;

// Debug flag for tracing function lookups
static bool g_trace_lookups = true;

// Forward declarations for all recompiled functions
''')

        # Write extern declarations
        for func_name, addr in functions:
            f.write(f'extern "C" void {func_name}(uint8_t* rdram, recomp_context* ctx);\n')

        f.write('''
void init_function_table() {
    g_function_table.clear();
    g_function_table.reserve(''' + str(len(functions)) + ''');

''')

        # Write table entries
        for func_name, addr in functions:
            f.write(f'    g_function_table[0x{addr:08X}] = {func_name};\n')

        f.write('''}

recomp_func_t* cvlod_get_function(int32_t vram) {
    auto it = g_function_table.find(vram);
    if (it != g_function_table.end()) {
        if (g_trace_lookups) {
            printf("[LOOKUP] 0x%08X -> found\\n", (uint32_t)vram);
        }
        return it->second;
    }

    // Function not found - this could be:
    // 1. A stubbed function
    // 2. A function we missed during recompilation
    // 3. An invalid address
    printf("[LOOKUP] 0x%08X -> NOT FOUND!\\n", (uint32_t)vram);
    return nullptr;
}

// Set a libultra wrapper function, overriding the stubbed recompiled version
void set_libultra_wrapper(int32_t vram, recomp_func_t* wrapper) {
    auto it = g_function_table.find(vram);
    if (it != g_function_table.end()) {
        printf("[WRAPPER] Replacing function at 0x%08X\\n", (uint32_t)vram);
    } else {
        printf("[WRAPPER] Adding function at 0x%08X\\n", (uint32_t)vram);
    }
    g_function_table[vram] = wrapper;
}

// Disable/enable function lookup tracing
void set_trace_lookups(bool enable) {
    g_trace_lookups = enable;
}

// Add address aliases for thread entry points.
// Some runtime thread entry addresses observed through osCreateThread do not
// match the ELF symbol addresses emitted by the recompile pipeline.
void add_address_aliases() {
    printf("Adding address aliases for thread entry points...\\n");
''')

        for alias_addr, target_addr, target_name, description in ADDRESS_ALIASES:
            f.write(f'''
    recomp_func_t* alias_{alias_addr:08X} = cvlod_get_function(0x{target_addr:08X});
    if (alias_{alias_addr:08X}) {{
        printf("  0x{alias_addr:08X} -> {target_name} ({description})\\n");
        g_function_table[0x{alias_addr:08X}] = alias_{alias_addr:08X};
    }}
''')

        f.write('''
    // Thread entry 0x80018B50 falls in the middle of a containing function in
    // current splits. Do not alias it to the containing function start; it needs
    // a real mid-function trampoline/label before it can be registered safely.
}
''')

def main():
    project_root = Path(__file__).parent.parent
    header_path = project_root / "recompiled" / "functions" / "funcs.h"
    output_path = project_root / "src" / "funcs.cpp"

    if not header_path.exists():
        print(f"Error: {header_path} not found")
        print("Run N64Recomp first to generate the recompiled code")
        sys.exit(1)

    print(f"Parsing {header_path}...")
    functions = parse_funcs_header(header_path)
    print(f"Found {len(functions)} functions")

    print(f"Generating {output_path}...")
    generate_cpp(functions, output_path)
    print("Done!")

    return 0

if __name__ == "__main__":
    sys.exit(main())
