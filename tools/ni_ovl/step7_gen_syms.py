#!/usr/bin/env python3
"""
Step 7: Generate syms.toml sections for NI overlays.
Overlays are classified by their compiled VRAM:
  - 0x0F000000: game object overlays (enemies, UI, etc.)
  - 0x0E000000: cutscene/textbox overlays
Classification is by internal jal targets (see vram_classification.json).

Usage: python3 tools/ni_ovl/step7_gen_syms.py
"""

import sys, json
from pathlib import Path

SCRIPT_DIR = Path(__file__).parent

def main():
    functions_path = SCRIPT_DIR / 'out' / 'functions.json'
    with open(functions_path) as f:
        all_functions = json.load(f)

    offsets_path = SCRIPT_DIR / 'out' / 'rom_offsets.json'
    with open(offsets_path) as f:
        rom_offsets = json.load(f)

    classification_path = SCRIPT_DIR / 'out' / 'vram_classification.json'
    with open(classification_path) as f:
        classification = json.load(f)

    pairs_0e = set(classification['0x0E_pairs'])

    # Build lookup by pair_index
    offset_map = {o['pair_index']: o for o in rom_offsets}

    toml_lines = []
    overlay_names = []
    total_funcs = 0
    count_0e = 0
    count_0f = 0

    toml_lines.append("# NI overlay sections - auto-generated by step7_gen_syms.py")
    toml_lines.append("# 0x0F = game object overlays, 0x0E = cutscene/textbox overlays")
    toml_lines.append("")

    for ovl in all_functions:
        pair_idx = ovl['pair_index']
        rom_info = offset_map[pair_idx]
        rom_offset = rom_info['rom_offset']
        text_size = ovl['text_size']
        functions = ovl['functions']

        # Determine VRAM base from classification
        if pair_idx in pairs_0e:
            vram_base = 0x0E000000
            count_0e += 1
        else:
            vram_base = 0x0F000000
            count_0f += 1

        section_name = f"..ni_ovl_{pair_idx:03d}"
        overlay_names.append(section_name)

        toml_lines.append(f"[[section]]")
        toml_lines.append(f'name = "{section_name}"')
        toml_lines.append(f"rom = 0x{rom_offset:08X}")
        toml_lines.append(f"vram = 0x{vram_base:08X}")
        toml_lines.append(f"size = 0x{text_size:X}")
        toml_lines.append("")
        toml_lines.append("functions = [")

        for func in functions:
            offset = func['offset']
            vram = vram_base + offset
            size = func['size']
            fname = f"ni_ovl_{pair_idx:03d}_func_{vram:08X}"
            toml_lines.append(f'    {{ name = "{fname}", vram = 0x{vram:08X}, size = 0x{size:X} }},')
            total_funcs += 1

        toml_lines.append("]")
        toml_lines.append("")

    # Write TOML
    toml_path = SCRIPT_DIR / 'out' / 'ni_ovl_sections.toml'
    with open(toml_path, 'w') as f:
        f.write('\n'.join(toml_lines))

    # Write overlay names
    overlays_path = SCRIPT_DIR / 'out' / 'ni_ovl_overlays.txt'
    with open(overlays_path, 'w') as f:
        f.write('\n'.join(overlay_names) + '\n')

    print(f"Generated {len(overlay_names)} sections with {total_funcs} functions")
    print(f"  0x0F (game objects): {count_0f}")
    print(f"  0x0E (cutscenes):    {count_0e}")
    print(f"  TOML: {toml_path}")
    print(f"  Overlays: {overlays_path}")

    # Validate: unique function names
    all_names = set()
    for ovl in all_functions:
        pair_idx = ovl['pair_index']
        vram_base = 0x0E000000 if pair_idx in pairs_0e else 0x0F000000
        for func in ovl['functions']:
            vram = vram_base + func['offset']
            fname = f"ni_ovl_{pair_idx:03d}_func_{vram:08X}"
            assert fname not in all_names, f"Duplicate: {fname}"
            all_names.add(fname)

    print(f"  Validation: OK (all names unique)")

if __name__ == '__main__':
    main()
